DATAS SEGMENT
	CMPAER DB 00000110B
	YES	   DB 'YES','$'
	NO     DB 'NO','$'
    ;此处输入数据段代码  
DATAS ENDS

STACKS SEGMENT
    ;此处输入堆栈段代码
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    
    MOV AX,1000001000000001B
    
    MOV CX,12
    
LOOP1:
	
	MOV BL,AL
	AND BL,0FH
	CMP BL,CMPAER
	JZ  EXIT0
	ROR AX,1
	LOOP LOOP1
	
EXIT0:

	CMP CX,0
	JZ  PRINT_NO
	JNZ PRINT_YES
	
PRINT_NO:
	
	MOV DX,OFFSET NO
	JMP EXIT1
		
PRINT_YES:

	MOV DX,OFFSET YES
	
EXIT1:
	
	MOV AH,09H
	INT 21H
    ;此处输入代码段代码
    MOV AH,4CH
    INT 21H
CODES ENDS
    END START
